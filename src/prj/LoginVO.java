package prj;

public class LoginVO {
	
	private String id ;
	private String password;

	
	
	
	public LoginVO(String id, String password) {
		super();
		this.id = id;
		this.password = password;

	}

	


	public String getId() {
		return id;
	}


	public String getPassword() {
		return password;
	}



	
	
	
	
	

}
